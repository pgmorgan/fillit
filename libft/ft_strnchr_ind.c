#include "libft.h"

int	ft_strnchr_ind(const char *s, int c, size_t n)
{
	int	i;

	i = 0;
	while (s[i] && n--)
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	return (-1);
}
