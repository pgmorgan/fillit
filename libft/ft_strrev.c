#include <unistd.h>

char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	b;

	i = 0;
	j = 0;
	while (str[i])
		++i;
	while (i > j)
	{
		b = str[i - 1];
		str[i - 1] = str[j];
		str[j] = b;
		--i;
		++j;
	}
	return (str);
}
